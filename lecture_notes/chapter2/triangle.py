# triangle.py

import math

# set the length of side 1 of the right triangle
side1 = 3

# set the length of side 2 of the right triangle
side2 = 4

# calculate the hypotenuse of the triangle using 
# the Pythagorean theorem
hypotenuse = math.sqrt(side1 ** 2 + side2 ** 2)

# print the results
print(hypotenuse)
